	        <br>
	        
	        <p id="1">一、玩法类型及承销</p>
                               中国福利彩票安徽省快三游戏（以下简称“快三”）由中国福利彩票发行管理中心和组织销售，安徽省福利彩票销售机构（以下称“安徽福彩机构”）在所辖区域内承销。

			<p id="2">二、 开奖与购买方式</p>
			1.  本站安徽快三游戏每天进行80期，开奖时间为08:50至22:00，每隔10分钟开奖一次。快三采用专用电子开奖设备开奖，每期随机生成3个号码，作为当期中奖号码。
			<br>2.  每期开奖后，相关安徽福彩机构应向社会公布开奖号码、将当期销售总额、开各奖级中奖情况及奖池资金余额等信息，并通过视频方式将开奖结果通知销售网点。
			<br>3.  购买者可在对其选定的投注号码选择返点投注，返点越高投注赔率则低。购买者也可以进行追号投注，追号是指将一注或一组号码进行两期或两期以上的投注。追号可分为连续追号和间隔追号，连续追号指追号的期数是连续的，间隔追号指追号的期数不连续。
			<br>4.  购买者可选择机选号码投注、自选号码投注。机选号码投注是指由投注机随机产生投注号码进行投注，自选号码投注是指将购买者选定的号码输入投注机进行投注。
			<br>5.  如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，应退还购买者投注金额。
			
			<p id="3">三、兑奖</p>
			1.  无论大小奖均返还至用户在本站的账户中，一旦用户中奖，系统将自动返还中奖金额。可继续投注或提款，永无弃奖。
			
			<p id="4">四、玩法说明</p>
			1.  快三投注是指以3个号码组合为一注彩票进行单式投注，每个投注号码为1-6共六个自然数中的任意一个。
