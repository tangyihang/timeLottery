	        <br>
	        
            <b><p id="1">一、玩法类型及承销</p></b>福彩3D是一种小盘玩法游戏，属数字型彩票范畴，由中国福利彩票发行管理中心统一组织发行，在全国范围内销售。
            <br>
            <br>
            
            <b><p id="2">二、开奖与购买方式</p></b>
                 1. 本站福彩3D游戏每天进行1期，每晚21:15现场开奖，开奖号码通过摇奖方式产生，中央广播电台中国之声对开奖进行现场直播。
            <br> 2. 购买者可透过本站进行投注，投注时需先选择相应的房间，不同房间赔率返点不一样，返点越高投注赔率则低。
            <br> 3. 如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，应退还购买者投注金额。
            <br>
            <br>
            
            <b><p id="3">三、兑奖</p></b> 1. 返奖：无论大小奖均返还至用户在本站的账户中，一旦用户中奖，系统将自动返还中奖金额。可继续投注或提款，永无弃奖。
            <br>
            <br>
            
            <b><p id="4">四、玩法说明</p></b>
                 1．福彩3D投注区分为百位、十位和个位，以一个3位自然数为投注号码的彩种玩法，投注者从000到999的范围内选择一个3位数进行投注，福彩3D玩法即是竞猜3位开奖号码。
